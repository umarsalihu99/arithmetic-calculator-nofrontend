let numb1 = parseInt(prompt("First Number: "));
let opr = prompt("Operator: ");
let numb2 = parseInt(prompt("Second Number: "));

if (opr == "+") {
    alert(numb1 + numb2)
} else if (opr == "-") {
    alert(numb1 - numb2)
} else if (opr == "*") {
    alert(numb1 * numb2)
} else if (opr == "/") {
    alert(numb1 / numb2)
}